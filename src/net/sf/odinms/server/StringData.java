package net.sf.odinms.server;



/**
 *
 * @author Admin
 */
public class StringData {

    private String type;
    private String name;

    public StringData(String type, String name) {
        this.type = type;
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public String getName() {
        return name;
    }
   
}




/**
 *
 * @author Owner
 */
public class TaoRankingInfo {
    private String name;
    private int rb, taocheck, rank, clan, clanrank;

    public TaoRankingInfo(String name, int clan, int rb, int taocheck, int rank, int clanrank) {
	this.name = name;
	this.clan = clan;
        this.rb = rb;
        this.taocheck = taocheck;
        this.rank = rank;
        this.clanrank = clanrank;
    }

    public int getClanrank() {
        return clanrank;
    }

    public String getName() {
	return name;
    }

    public int getClan() {
	return clan;
    }

    public int getRB() {
	return rb;
    }

    public int getTaoCheck() {
	return taocheck;
    }

    public int getRank(){
        return rank;
    }
}
